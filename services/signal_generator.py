    @staticmethod
    def _route_target(proposal: TradeProposal) -> str:
        """Determine which bot style should handle this proposal."""
        if proposal.priority == SignalPriority.SWING:
            return "swing"
        return "momentum"

    def _propose(self, queue: TradeQueue, proposal: TradeProposal) -> None:
        """Add proposal if not on cooldown and not a live duplicate.

        Applies analytics strength modifier before queuing — underperforming
        strategies get progressively weaker signals.
        """
        if not self._symbol_tradeable(proposal.symbol):
            logger.warning("Skipping {}: not on any known exchange", proposal.symbol)
            return

        if self._rejection_cooldown_active(proposal.symbol, proposal.strategy):
            return

        if not proposal.target_bot:
            proposal.target_bot = self._route_target(proposal)
        if proposal.market_type == "futures" and self._preferred_market_type != "futures":
            proposal.market_type = self._preferred_market_type

        proposal.supported_exchanges = self._supported_exchanges(proposal.symbol)

        # Apply analytics modifier to strength
        raw_strength = proposal.strength
        modifier = self._analytics_strength_modifier(proposal.strategy, proposal.symbol, is_quick=proposal.quick_trade)
        proposal.strength = round(raw_strength * modifier, 4)

        key = f"{proposal.priority.value}_{proposal.symbol}_{proposal.strategy}"

        if key in self._recent_ids:
            cooldown = self._cooldown_seconds[proposal.priority]
            elapsed = (datetime.now(UTC) - self._recent_ids[key]).total_seconds()
            if elapsed < cooldown:
                return

        for ex in queue.get_actionable(proposal.priority):
            if ex.symbol == proposal.symbol and ex.strategy == proposal.strategy:
                return

        queue.add(proposal)
        self._recent_ids[key] = datetime.now(UTC)
        all_ex = set(self._exchange_symbols.keys()) if self._exchange_symbols else set()
        missing = sorted(all_ex - set(proposal.supported_exchanges))
        exchange_note = f" (not on: {', '.join(missing)})" if missing else ""
        analytics_note = f" [analytics: {modifier:.2f}x]" if modifier != 1.0 else ""
        logger.info(
            "QUEUE [{}] {} {} — {} (str={:.2f}){}{}",
            proposal.priority.value.upper(),
            proposal.side.upper(),
            proposal.symbol,
            proposal.reason,
            proposal.strength,
            analytics_note,
            exchange_note,
        )

    def _purge_cooldowns(self) -> None:
        cutoff = datetime.now(UTC) - timedelta(hours=24)
        self._recent_ids = {k: v for k, v in self._recent_ids.items() if v > cutoff}

    def _merge_trending(self, snap: IntelSnapshot) -> list[TrendingSnapshot]:
        """Deduplicate trending coins from all sources."""
        seen: set[str] = set()
        result: list[TrendingSnapshot] = []
        for coin in snap.hot_movers + snap.cmc_trending + snap.coingecko_trending:
            if coin.symbol.upper() not in seen:
                seen.add(coin.symbol.upper())
                result.append(coin)
        return result

    def _count_directional_agreement(self, snap: IntelSnapshot) -> int:
        """Count how many independent intel sources agree on the preferred direction."""
        target = snap.preferred_direction
        if target == "neutral":
            return 0

        count = 0
        if snap.fear_greed_bias == target:
            count += 1
        if snap.liquidation_bias == target:
            count += 1
        if snap.whale_bias == target:
            count += 1
        if snap.tv_btc_consensus == target:
            count += 1
        if snap.regime == "risk_on" and target == "long":
            count += 1
        if snap.regime == "risk_off" and target == "short":
            count += 1
        return count

    def _get_tv_analysis(self, snap: IntelSnapshot, symbol: str):
        for tv in snap.tv_analyses:
            if tv.symbol == symbol and tv.interval == "1h":
                return tv
        return None

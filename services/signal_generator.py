"""Signal generator — converts intel + market data into prioritised trade proposals.

Runs inside the MonitorService after each data refresh.  Produces
TradeProposal objects at three priority tiers:

  CRITICAL  — act within seconds (spikes, mass-liq reversals, wick scalps)
  DAILY     — valid for hours (momentum entries, trending setups, TV alignment)
  SWING     — limit-order plans valid for days with full entry/exit blueprints

Analytics feedback loop:
  The monitor feeds the latest AnalyticsSnapshot each tick.  The generator
  uses strategy weights, detected patterns, and modification suggestions to
  modulate proposal strength, skip losing combos, and boost outperformers.

The proposals are stored in HubState (in-memory) and delivered to bots
via the /internal/report HTTP response.
"""

from __future__ import annotations

from datetime import UTC, datetime, timedelta
from typing import TYPE_CHECKING

from loguru import logger

from shared.models import (
    AnalyticsSnapshot,
    EntryPlan,
    IntelSnapshot,
    SignalPriority,
    TradeProposal,
    TradeQueue,
    TrendingSnapshot,
)

if TYPE_CHECKING:
    from hub.candle_fetcher import CandleFetcher

MAJOR_SYMBOLS = {"BTC/USDT", "ETH/USDT", "SOL/USDT", "XRP/USDT", "DOGE/USDT", "ADA/USDT", "AVAX/USDT", "LINK/USDT"}

# Strategies generated by the signal generator don't map 1:1 to strategy names
# in the analytics DB.  This maps signal-generator strategy names to the bot
# strategies whose performance data is most relevant.
_STRATEGY_ANALYTICS_MAP: dict[str, str] = {
    "liq_reversal": "compound_momentum",
    "macro_spike": "market_open_volatility",
    "extreme_mover": "compound_momentum",
    "trending_momentum": "compound_momentum",
    "fear_accumulation": "swing_opportunity",
    "multi_intel_convergence": "compound_momentum",
    "major_momentum": "compound_momentum",
    "major_intel_direction": "compound_momentum",
    "overleveraged_fade": "mean_reversion",
    "capitulation_dip_buy": "swing_opportunity",
    "greed_reversal_plan": "swing_opportunity",
    "eth_rotation_play": "swing_opportunity",
    "major_swing": "swing_opportunity",
    "trending_filler": "compound_momentum",
    "major_filler": "compound_momentum",
}


class SignalGenerator:
    """Stateful generator that avoids duplicate proposals via cooldowns.

    Analytics-aware: reads strategy weights, patterns, and suggestions to
    continuously modulate signal strength.  Nothing is ever blocked — losing
    strategies just get progressively weaker signals until they're effectively
    below the bot's minimum threshold.
    """

    MIN_QUEUE_SIZE = 10

    def __init__(self, preferred_market_type: str = "futures", major_symbols: set[str] | None = None) -> None:
        self._preferred_market_type = preferred_market_type
        self._major_symbols = major_symbols or MAJOR_SYMBOLS
        self._recent_ids: dict[str, datetime] = {}
        self._cooldown_seconds = {
            SignalPriority.CRITICAL: 30,
            SignalPriority.DAILY: 900,
            SignalPriority.SWING: 14400,
        }
        self._exchange_symbols: dict[str, set[str]] = {}  # {EXCHANGE: {symbols}}
        self._all_tradeable: set[str] = set()  # union of all exchange symbols

        # Rejection feedback — populated by monitor from HubState each tick.
        # Maps "symbol|strategy" → (reason, timestamp, consecutive_count).
        self._rejection_history: dict[str, tuple[str, datetime, int]] = {}
        self._rejection_base_cooldown = 300  # 5min base, doubles per consecutive rejection
        self._rejection_max_cooldown = 7200  # cap at 2 hours

        # Analytics feedback state (updated by monitor each tick)
        self._analytics: AnalyticsSnapshot | None = None
        self._strategy_weights: dict[str, float] = {}  # analytics engine weight per strategy
        self._strategy_streaks: dict[str, int] = {}  # current streak per strategy
        self._combo_penalties: dict[tuple[str, str], float] = {}  # (strategy, symbol) -> multiplier
        self._global_bad_regimes: set[str] = set()  # regimes with high loss rate (all strats)
        self._global_bad_hours: set[int] = set()  # hours with high loss rate (all strats)
        self._strat_bad_regimes: dict[str, set[str]] = {}  # per-strategy bad regimes
        self._strat_bad_hours: dict[str, set[int]] = {}  # per-strategy bad hours
        self._quick_trade_penalty: float = 1.0  # multiplier for scalp/quick proposals
        self._current_regime: str = "normal"  # set each generate() call
        self._current_hour: int = -1  # set each generate() call

    # ------------------------------------------------------------------
    # Analytics feedback
    # ------------------------------------------------------------------

    def update_analytics(self, snap: AnalyticsSnapshot) -> None:
        """Ingest the latest analytics snapshot and rebuild lookup tables.

        Called by the monitor each tick.  Everything here becomes a soft
        strength multiplier — nothing is ever hard-blocked.
        """
        self._analytics = snap
        self._strategy_weights.clear()
        self._strategy_streaks.clear()
        self._combo_penalties.clear()
        self._global_bad_regimes.clear()
        self._global_bad_hours.clear()
        self._strat_bad_regimes.clear()
        self._strat_bad_hours.clear()
        self._quick_trade_penalty = 1.0

        for w in snap.weights:
            self._strategy_weights[w.strategy] = w.weight
            self._strategy_streaks[w.strategy] = w.streak

        for pat in snap.patterns:
            ptype = pat.get("pattern_type", "")
            data = pat.get("data", {})

            if ptype == "market_regime":
                regime = data.get("regime", "")
                if regime:
                    self._global_bad_regimes.add(regime)

            elif ptype == "time_of_day":
                hour = data.get("hour")
                if hour is not None:
                    self._global_bad_hours.add(int(hour))

            elif ptype == "strategy_symbol":
                strat = pat.get("affected_strategy", "")
                sym = pat.get("affected_symbol", "")
                loss_rate = data.get("loss_rate", 0.0)
                if strat and sym and loss_rate > 0:
                    penalty = max(0.2, 1.0 - loss_rate)
                    self._combo_penalties[(strat, sym)] = penalty

            elif ptype == "quick_trade":
                loss_rate = data.get("loss_rate", 0.0)
                if loss_rate > 0.5:
                    self._quick_trade_penalty = max(0.3, 1.0 - loss_rate)

        for sug in snap.suggestions:
            stype = sug.get("suggestion_type", "")
            strat = sug.get("strategy", "")
            if not strat:
                continue

            if stype == "regime_filter":
                val = sug.get("suggested_value", "")
                regime = val.replace("skip ", "").strip()
                if regime:
                    self._strat_bad_regimes.setdefault(strat, set()).add(regime)

            elif stype == "time_filter":
                val = sug.get("suggested_value", "")
                try:
                    hour = int(val.replace("skip hour ", "").strip())
                    self._strat_bad_hours.setdefault(strat, set()).add(hour)
                except (ValueError, TypeError):
                    pass

        if snap.weights:
            logger.debug(
                "Analytics loaded: {} strategies, {} patterns, {} suggestions | global bad regimes={} bad hours={}",
                len(snap.weights),
                len(snap.patterns),
                len(snap.suggestions),
                self._global_bad_regimes or "none",
                self._global_bad_hours or "none",
            )

    def update_rejections(self, rejections: dict[str, tuple[str, datetime, int]]) -> None:
        """Ingest rejection history from HubState.

        Called by the monitor each tick. The signal generator uses this to
        apply escalating cooldowns on symbol+strategy combos that bots keep
        rejecting — breaks the propose→reject→propose loop.
        """
        self._rejection_history = rejections

    def _rejection_cooldown_active(self, symbol: str, strategy: str) -> bool:
        """Check if a symbol+strategy is on rejection cooldown."""
        rkey = f"{symbol}|{strategy}"
        entry = self._rejection_history.get(rkey)
        if not entry:
            return False
        _reason, ts, count = entry
        cooldown = min(self._rejection_base_cooldown * (2 ** (count - 1)), self._rejection_max_cooldown)
        elapsed = (datetime.now(UTC) - ts).total_seconds()
        if elapsed < cooldown:
            logger.debug(
                "Rejection cooldown active for {}|{} (rejected {}x, {}s remaining)",
                symbol,
                strategy,
                count,
                int(cooldown - elapsed),
            )
            return True
        return False

    def _analytics_strength_modifier(self, strategy: str, symbol: str, is_quick: bool = False) -> float:
        """Compute a strength multiplier from analytics feedback.

        Returns a value in [0.3, 1.5].  Never zero — nothing is blocked,
        just progressively de-prioritised as evidence accumulates against it.
        """
        if not self._analytics or not self._analytics.weights:
            return 1.0

        analytics_name = _STRATEGY_ANALYTICS_MAP.get(strategy, strategy)

        base_weight = self._strategy_weights.get(analytics_name, 1.0)
        modifier = base_weight

        streak = self._strategy_streaks.get(analytics_name, 0)
        if streak <= -5:
            modifier *= 0.5
        elif streak <= -3:
            modifier *= 0.7

        if self._current_regime in self._global_bad_regimes:
            modifier *= 0.7
        if self._current_regime in self._strat_bad_regimes.get(analytics_name, set()):
            modifier *= 0.6

        if self._current_hour in self._global_bad_hours:
            modifier *= 0.75
        if self._current_hour in self._strat_bad_hours.get(analytics_name, set()):
            modifier *= 0.6

        combo_key = (analytics_name, symbol)
        if combo_key in self._combo_penalties:
            modifier *= self._combo_penalties[combo_key]

        if is_quick and self._quick_trade_penalty < 1.0:
            modifier *= self._quick_trade_penalty

        return max(0.3, min(1.5, modifier))

    # ------------------------------------------------------------------
    # Exchange symbol tracking
    # ------------------------------------------------------------------

    def update_exchange_symbols(self, exchange_symbols: dict[str, set[str]]) -> None:
        """Update known exchange symbol sets (called by monitor each tick)."""
        self._exchange_symbols = exchange_symbols
        self._all_tradeable = set()
        for syms in exchange_symbols.values():
            self._all_tradeable |= syms

    def _symbol_tradeable(self, symbol: str) -> bool:
        """Check if at least one exchange supports this symbol."""
        if not self._all_tradeable:
            return False  # no exchange data yet — wait for bots to report
        return symbol in self._all_tradeable

    def _supported_exchanges(self, symbol: str) -> list[str]:
        """Return list of exchanges that have this symbol."""
        if not self._exchange_symbols:
            return []
        return [ex for ex, syms in self._exchange_symbols.items() if symbol in syms]

    def generate(self, snap: IntelSnapshot, queue: TradeQueue) -> TradeQueue:
        """Evaluate the current intel snapshot and append new proposals."""
        self._current_regime = snap.regime
        self._current_hour = datetime.now(UTC).hour

        self._purge_cooldowns()
        queue.purge_stale()

        self._generate_critical(snap, queue)
        self._generate_daily(snap, queue)
        self._generate_swing(snap, queue)
        self._generate_filler(snap, queue)

        return queue

    # ------------------------------------------------------------------
    # CRITICAL — seconds to act
    # ------------------------------------------------------------------

    def _generate_critical(self, snap: IntelSnapshot, q: TradeQueue) -> None:
        # Mass liquidation reversal — contrarian entry on capitulation
        if snap.mass_liquidation:
            side = snap.liquidation_bias  # "long" when longs liquidated (buy dip)
            if side in ("long", "short"):
                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.CRITICAL,
                        symbol="BTC/USDT",
                        side=side,
                        strategy="liq_reversal",
                        reason=f"Mass liq ${snap.liquidation_24h / 1e9:.1f}B — bias {side} (exhaustion reversal)",
                        strength=0.85,
                        leverage=10,
                        quick_trade=True,
                        max_hold_minutes=15,
                        max_age_seconds=120,
                        source="monitor",
                    ),
                )

        # Macro spike opportunity — FOMC/CPI just dropped, expect volatility
        if snap.macro_spike_opportunity:
            direction = snap.preferred_direction
            if direction in ("long", "short"):
                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.CRITICAL,
                        symbol="BTC/USDT",
                        side=direction,
                        strategy="macro_spike",
                        reason=f"Macro event spike — {snap.next_macro_event}",
                        strength=0.75,
                        leverage=10,
                        quick_trade=True,
                        max_hold_minutes=10,
                        max_age_seconds=60,
                        source="monitor",
                    ),
                )

        # Hot mover with extreme 1h move — scalp the momentum
        for mover in snap.hot_movers[:5]:
            if abs(mover.change_1h) >= 8.0 and not mover.is_low_liquidity:
                side = "long" if mover.change_1h > 0 else "short"
                sym = f"{mover.symbol.upper()}/USDT"
                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.CRITICAL,
                        symbol=sym,
                        side=side,
                        strategy="extreme_mover",
                        reason=f"{mover.symbol} moved {mover.change_1h:+.1f}% in 1h",
                        strength=min(0.9, abs(mover.change_1h) / 12.0),
                        leverage=10,
                        quick_trade=True,
                        max_hold_minutes=10,
                        max_age_seconds=90,
                        source="monitor",
                    ),
                )

    # ------------------------------------------------------------------
    # DAILY — valid for hours
    # ------------------------------------------------------------------

    def _generate_daily(self, snap: IntelSnapshot, q: TradeQueue) -> None:
        # Trending coins with TV alignment — momentum entry
        tv_direction = snap.tv_btc_consensus
        for mover in self._merge_trending(snap):
            if mover.is_low_liquidity:
                continue
            if abs(mover.change_24h) < 2.0:
                continue

            side = "long" if mover.change_24h > 0 else "short"
            sym = f"{mover.symbol.upper()}/USDT"

            tv_aligned = (tv_direction == side) or tv_direction == "neutral"
            strength = 0.40
            if abs(mover.change_24h) >= 5.0:
                strength += 0.10
            if tv_aligned:
                strength += 0.15
            if abs(mover.change_24h) > 10:
                strength += 0.10

            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.DAILY,
                    symbol=sym,
                    side=side,
                    strategy="trending_momentum",
                    reason=f"{mover.symbol} 24h:{mover.change_24h:+.1f}% vol:${mover.volume_24h / 1e6:.0f}M"
                    f" TV:{tv_direction}",
                    strength=min(0.9, strength),
                    leverage=10,
                    max_age_seconds=14400,  # 4 hours
                    source="monitor",
                ),
            )

        # Fear zone BTC buy — accumulate when fearful
        if snap.fear_greed <= 30 and snap.preferred_direction == "long":
            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.DAILY,
                    symbol="BTC/USDT",
                    side="long",
                    strategy="fear_accumulation",
                    reason=f"F&G={snap.fear_greed} (fear) — contrarian buy zone",
                    strength=0.65,
                    leverage=5,
                    max_age_seconds=7200,
                    source="monitor",
                ),
            )

        # Multiple intel sources agree on direction — high-conviction entry
        if snap.preferred_direction in ("long", "short"):
            aligned_sources = self._count_directional_agreement(snap)
            if aligned_sources >= 3:
                for sym in ("BTC/USDT", "ETH/USDT"):
                    self._propose(
                        q,
                        TradeProposal(
                            priority=SignalPriority.DAILY,
                            symbol=sym,
                            side=snap.preferred_direction,
                            strategy="multi_intel_convergence",
                            reason=f"{aligned_sources} intel sources agree: "
                            f"{snap.preferred_direction} (regime={snap.regime})",
                            strength=min(0.85, 0.5 + aligned_sources * 0.1),
                            leverage=10,
                            max_age_seconds=7200,
                            source="monitor",
                        ),
                    )

        # Major coins momentum — lower threshold than altcoins (2% vs 5%)
        for mover in self._merge_trending(snap):
            sym = f"{mover.symbol.upper()}/USDT"
            if sym not in self._major_symbols:
                continue
            if abs(mover.change_24h) < 2.0:
                continue

            side = "long" if mover.change_24h > 0 else "short"
            strength = 0.50
            if tv_direction == side:
                strength += 0.15
            if abs(mover.change_24h) > 5:
                strength += 0.15
            if snap.fear_greed_bias == side:
                strength += 0.05

            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.DAILY,
                    symbol=sym,
                    side=side,
                    strategy="major_momentum",
                    reason=f"{mover.symbol} 24h:{mover.change_24h:+.1f}% (major, lower threshold)",
                    strength=min(0.85, strength),
                    leverage=10,
                    max_age_seconds=14400,
                    source="monitor",
                ),
            )

        # Major coins intel-driven daily — propose when intel agrees even without big moves
        if snap.preferred_direction in ("long", "short"):
            for sym in self._major_symbols:
                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.DAILY,
                        symbol=sym,
                        side=snap.preferred_direction,
                        strategy="major_intel_direction",
                        reason=f"Intel direction {snap.preferred_direction} for {sym} (regime={snap.regime})",
                        strength=0.45,
                        leverage=5,
                        max_age_seconds=7200,
                        source="monitor",
                    ),
                )

        # Overleveraged fade — contrarian against crowded positioning
        if snap.overleveraged_side in ("longs", "shorts"):
            fade_side = "short" if snap.overleveraged_side == "longs" else "long"
            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.DAILY,
                    symbol="BTC/USDT",
                    side=fade_side,
                    strategy="overleveraged_fade",
                    reason=f"{snap.overleveraged_side} overleveraged — fade towards squeeze",
                    strength=0.55,
                    leverage=5,
                    max_age_seconds=14400,
                    source="monitor",
                ),
            )

    # ------------------------------------------------------------------
    # SWING — limit order plans with full entry blueprint
    # ------------------------------------------------------------------

    def _generate_swing(self, snap: IntelSnapshot, q: TradeQueue) -> None:
        # Extreme fear + heavy liquidations = "opportunity of a lifetime" candidate
        if snap.fear_greed <= 15 and snap.mass_liquidation:
            btc_tv = self._get_tv_analysis(snap, "BTC/USDT")
            rsi = btc_tv.rsi_14 if btc_tv else 30.0

            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.SWING,
                    symbol="BTC/USDT",
                    side="long",
                    strategy="capitulation_dip_buy",
                    reason=f"Extreme fear ({snap.fear_greed}) + mass liq "
                    f"${snap.liquidation_24h / 1e9:.1f}B + RSI {rsi:.0f} — "
                    f"potential generational bottom",
                    strength=0.9,
                    leverage=10,
                    max_age_seconds=259200,  # 3 days
                    entry_plan=EntryPlan(
                        entry_zone_low=0,
                        entry_zone_high=0,
                        stop_loss=0,
                        take_profit_targets=[],
                        dca_levels=[],
                        initial_leverage=5,
                        max_leverage=10,
                        scale_in_pct=3.0,
                        notes="Capitulation event. PYRAMID mode: start small, DCA into "
                        "wicks, lever up on recovery. Add at -3%, -5%, -8%. "
                        "Move stop to break-even at +5%. Let it ride.",
                    ),
                    source="monitor",
                ),
            )

        # Caution regime + greed = short setup with planned entries
        if snap.regime in ("risk_off", "caution") and snap.fear_greed >= 70:
            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.SWING,
                    symbol="BTC/USDT",
                    side="short",
                    strategy="greed_reversal_plan",
                    reason=f"Risk-off regime + F&G={snap.fear_greed} (greed) — planned short if momentum breaks",
                    strength=0.6,
                    leverage=5,
                    max_age_seconds=172800,  # 2 days
                    entry_plan=EntryPlan(
                        initial_leverage=5,
                        max_leverage=10,
                        scale_in_pct=2.0,
                        notes="Wait for momentum break (lower high on 4h). "
                        "Enter on confirmed rejection. Tight stop above "
                        "recent high. Take profit at fear zone (F&G < 40). "
                        "Trail 2% below each new lower high.",
                    ),
                    source="monitor",
                ),
            )

        # ETH swing when strong BTC + ETH lagging (rotation play)
        btc_tv = self._get_tv_analysis(snap, "BTC/USDT")
        eth_tv = self._get_tv_analysis(snap, "ETH/USDT")
        if btc_tv and eth_tv:
            btc_strong = btc_tv.consensus == "long"
            eth_weak = eth_tv.consensus in ("neutral", "short")
            if btc_strong and eth_weak and snap.fear_greed < 60:
                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.SWING,
                        symbol="ETH/USDT",
                        side="long",
                        strategy="eth_rotation_play",
                        reason="BTC strong, ETH lagging — rotation play when ETH catches up",
                        strength=0.55,
                        leverage=5,
                        max_age_seconds=259200,
                        entry_plan=EntryPlan(
                            initial_leverage=3,
                            max_leverage=8,
                            scale_in_pct=2.0,
                            notes="Enter when ETH/BTC ratio starts turning up. "
                            "Use limit orders at recent support. "
                            "DCA if it dips further. Trail stop at 4%.",
                        ),
                        source="monitor",
                    ),
                )

        # Major coins structural swing — always keep the SWING queue populated
        # for majors based on broader market regime + technical confluence.
        self._generate_major_swings(snap, q)

    # ------------------------------------------------------------------
    # Major Coin Swing Proposals
    # ------------------------------------------------------------------

    def _generate_major_swings(self, snap: IntelSnapshot, q: TradeQueue) -> None:
        """Generate swing proposals for major coins based on regime + technicals.

        Unlike the altcoin trending loop, this runs for all major symbols on
        every cycle. Majors are steadier — they don't need 5%+ moves to be
        tradeable; regime alignment and technical confluence are enough.
        """
        direction = snap.preferred_direction
        if direction not in ("long", "short"):
            return

        regime_ok = (direction == "long" and snap.regime in ("risk_on", "neutral")) or (
            direction == "short" and snap.regime in ("risk_off", "caution")
        )
        if not regime_ok:
            return

        aligned = self._count_directional_agreement(snap)
        if aligned < 2:
            return

        strength = 0.40 + aligned * 0.08
        fg = snap.fear_greed
        if (direction == "long" and fg <= 35) or (direction == "short" and fg >= 65):
            strength += 0.10

        for sym in self._major_symbols:
            tv = self._get_tv_analysis(snap, sym)
            sym_aligned = tv and tv.consensus == direction
            sym_strength = strength + (0.10 if sym_aligned else 0.0)

            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.SWING,
                    symbol=sym,
                    side=direction,
                    strategy="major_swing",
                    reason=f"{sym} swing: {aligned} intel agree {direction}, regime={snap.regime}, F&G={fg}",
                    strength=min(0.85, sym_strength),
                    leverage=10,
                    max_age_seconds=172800,
                    entry_plan=EntryPlan(
                        initial_leverage=5,
                        max_leverage=10,
                        scale_in_pct=2.0,
                        notes=f"Major coin swing {direction}. Enter at support/resistance "
                        f"confirmation. DCA on dips. Trail stop 3-4%.",
                    ),
                    source="monitor",
                ),
            )

    # ------------------------------------------------------------------
    # Filler — keep queue populated with best-available opportunities
    # ------------------------------------------------------------------

    def _generate_filler(self, snap: IntelSnapshot, q: TradeQueue) -> None:
        """Ensure at least MIN_QUEUE_SIZE pending proposals exist.

        Runs after all other generators.  Picks the best trending coins
        that weren't already proposed, using lower strength values so
        bots can decide whether to act.
        """
        pending = q.pending_count
        if pending >= self.MIN_QUEUE_SIZE:
            return

        needed = self.MIN_QUEUE_SIZE - pending
        tv_direction = snap.tv_btc_consensus
        candidates = self._merge_trending(snap)
        candidates.sort(key=lambda m: abs(m.change_24h), reverse=True)

        existing_syms = {
            p.symbol
            for bucket in (q.critical, q.daily, q.swing)
            for p in bucket
            if not p.consumed and not p.rejected and not p.is_expired
        }

        added = 0
        for mover in candidates:
            if added >= needed:
                break
            sym = f"{mover.symbol.upper()}/USDT"
            if sym in existing_syms:
                continue
            if mover.is_low_liquidity:
                continue

            side = "long" if mover.change_24h > 0 else "short"
            strength = 0.30 + min(0.25, abs(mover.change_24h) / 40.0)
            if tv_direction == side:
                strength += 0.10

            self._propose(
                q,
                TradeProposal(
                    priority=SignalPriority.DAILY,
                    symbol=sym,
                    side=side,
                    strategy="trending_filler",
                    reason=f"{mover.symbol} 24h:{mover.change_24h:+.1f}% vol:${mover.volume_24h / 1e6:.0f}M"
                    f" (best available)",
                    strength=min(0.65, strength),
                    leverage=5,
                    max_age_seconds=7200,
                    source="monitor",
                ),
            )
            existing_syms.add(sym)
            added += 1

        if added < needed:
            for sym in self._major_symbols:
                if added >= needed:
                    break
                if sym in existing_syms:
                    continue
                direction = snap.preferred_direction
                if direction not in ("long", "short"):
                    direction = "long" if snap.fear_greed < 50 else "short"

                self._propose(
                    q,
                    TradeProposal(
                        priority=SignalPriority.DAILY,
                        symbol=sym,
                        side=direction,
                        strategy="major_filler",
                        reason=f"{sym} regime={snap.regime} F&G={snap.fear_greed} (best available major)",
                        strength=0.35,
                        leverage=5,
                        max_age_seconds=7200,
                        source="monitor",
                    ),
                )
                existing_syms.add(sym)
                added += 1

